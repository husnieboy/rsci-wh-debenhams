function hexto5250($hexstring){

	$len = strlen($hexstring);
	$binary_string = "";
	for($i = 0; $i<=$len-2; $i+=2){
		$byte = substr($hexstring,$i,2);
		$binary_string.= chr(hexdec($byte));
	}
	return $binary_string;
}

