function myebcdictoascii($str){
	global $ebcdic;
	$len = strlen($str);
	$new_str = "";
	
	for($i = 0; $i<$len; $i++){
		$index = ord($str[$i]);
		$new_str .= chr($ebcdic[$index]);
	}
	return $new_str;
}

